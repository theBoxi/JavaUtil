package ch.boxi.javaUtil.map;

import java.util.Map.Entry;

public class SimpleMapEntry<K extends Comparable<K>, V> implements Entry<K, V>, Comparable<SimpleMapEntry<K, V>>{
	private K key;
	private V value;
	
	public SimpleMapEntry(K key, V value){
		this.key = key;
		this.value = value;
	}
	
	@Override
	public K getKey() {
		return key;
	}

	@Override
	public V getValue() {
		return value;
	}

	@Override
	public V setValue(V value) {
		V oldValue = this.value;
		this.value = value;
		return oldValue;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((key == null) ? 0 : key.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		SimpleMapEntry other = (SimpleMapEntry) obj;
		if (key == null) {
			if (other.key != null)
				return false;
		} else if (!key.equals(other.key))
			return false;
		return true;
	}

	@Override
	public int compareTo(SimpleMapEntry<K, V> o) {
		return key.compareTo(o.getKey());
	}
}
